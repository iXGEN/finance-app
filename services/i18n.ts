const translations: Record<Locale, Translations> = { es, en };

/** Non-reactive accessor for use outside React (services, the AI prompt). */
export function tFor(locale: Locale): Translations {
  return translations[locale];
}

/** Reactive translations for the current locale; re-renders on language change. */
export function useT(): Translations {
  return translations[useLocaleStore((s) => s.locale)];
}
