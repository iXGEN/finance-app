// Widen string literals to `string` and normalize function returns to `string`,
// while preserving argument types — so other locales can supply their own text
// without TypeScript demanding the exact Spanish literal.
type Widen<T> = T extends (...args: infer A) => any
  ? (...args: A) => string
  : T extends object
    ? { [K in keyof T]: Widen<T[K]> }
    : string;

